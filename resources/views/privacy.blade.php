            <h2>What This Service Is</h2>
            <p>This website hosts an AI-powered hiring assistant that represents James Gifford in conversations with potential employers, recruiters, and hiring managers. It is not a human — it is an automated system powered by large language models.</p>
            <p>The service is operated by Progravity LLC, an Oregon-based software company founded by James Gifford.</p>

            <h2>What Data We Collect</h2>
            <p>When you interact with this assistant via the web chat, email, or SMS, we collect and store:</p>
            <ul>
                <li><strong>Message content</strong>: The text of your messages and the assistant's responses, stored to maintain conversation context across multiple exchanges.</li>
                <li><strong>Session identifier</strong>: Your email address (for email conversations), phone number (for SMS conversations), or a randomly generated session token (for web chat).</li>
                <li><strong>Channel metadata</strong>: Which channel you used to communicate (web, email, or SMS), timestamps, and email subject lines where applicable.</li>
                <li><strong>Technical metadata</strong>: For SMS, your approximate geographic location (city, state, country) as provided by Twilio. For web chat, standard web server logs including IP address and browser user agent.</li>
            </ul>
            <p>We do <strong>not</strong> collect or store:</p>
            <ul>
                <li>Passwords, financial information, or government-issued identification</li>
                <li>Data from any other accounts or services</li>
                <li>Cookies for tracking or advertising purposes</li>
            </ul>

            <h2>How We Use Your Data</h2>
            <p>Your data is used solely to:</p>
            <ul>
                <li>Process your messages and generate AI responses within the context of our conversation</li>
                <li>Maintain conversation history so the assistant can reference earlier messages in the same conversation</li>
                <li>Monitor service health and debug technical issues</li>
            </ul>
            <p>Your message content is sent to third-party AI providers (Anthropic and/or OpenAI) for processing. These providers process the data according to their respective privacy policies and API data usage terms. Under their API terms, your data is not used to train their models.</p>
            <p>We do <strong>not</strong>:</p>
            <ul>
                <li>Sell, rent, or share your data with third parties for marketing purposes</li>
                <li>Use your data for advertising</li>
                <li>Use your data to train AI models</li>
                <li>Contact you unless you initiate a conversation first</li>
            </ul>

            <h2>Data Retention</h2>
            <p>Conversation data is retained for up to 90 days to support ongoing conversations and service improvement, after which it is deleted. You may request immediate deletion of your conversation data at any time by contacting <a href="mailto:james@jamesgifford.com">james@jamesgifford.com</a>.</p>

            <h2>SMS-Specific Disclosures</h2>
            <p>If you interact with this service via SMS:</p>
            <ul>
                <li>You consent to receiving messages by texting our number first. We never send unsolicited messages.</li>
                <li>Message and data rates may apply depending on your mobile carrier and plan.</li>
                <li>You can opt out at any time by replying <strong>STOP</strong>. You will receive a confirmation and no further messages will be sent.</li>
                <li>You can reply <strong>HELP</strong> for assistance information.</li>
                <li>Message frequency varies based on your conversation — we only reply to messages you send.</li>
                <li>Your phone number is stored to maintain your conversation history and is not shared with third parties.</li>
            </ul>

